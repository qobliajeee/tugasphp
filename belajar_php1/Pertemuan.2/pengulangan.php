<?php

// for
// while
// do while
// foreach

// langkah-langkah melakukan pengulangan
// 1. menentukan nilai awal
// 2. menetukan kondisi terminasi
// 3. menulis increment / decrement

// for
for($nilaiAwal = 1; $nilaiAwal <= 10; $nilaiAwal++){
    echo "Angka ke - $nilaiAwal <br />";
}

// while
$noAngkot = 1;
while($noAngkot <= 25){
     echo "No angkot ke - $noAngkot <br />";

     $noAngkot++;
}
?>